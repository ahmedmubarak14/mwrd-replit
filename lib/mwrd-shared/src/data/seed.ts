function makeProd(seq: number, name_en: string, name_ar: string, category_id: string, specs: Record<string,string>, pack_types: MasterProduct['pack_types'] = ['Each'], images?: string[], description_en?: string): MasterProduct {
  const id = `prod-${seq.toString().padStart(4, '0')}`;
  return {
    id,
    master_product_code: generateMasterProductCode(seq),
    name_en,
    name_ar,
    description_en: description_en ?? `${name_en} for office and business use`,
    description_ar: `${name_ar} للاستخدام المكتبي والتجاري`,
    category_id,
    specs,
    images: images ?? [`https://picsum.photos/seed/${id}/400/300`],
    pack_types,
    default_unit: pack_types[0] ?? 'Each',
    status: 'active',
    created_by_admin_id: ADMIN_ID,
    created_at: NOW,
    updated_at: NOW,
  };
}

